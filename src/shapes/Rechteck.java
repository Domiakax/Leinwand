package shapes;

public class Rechteck {
	
	int breite, hoehe, positionX, positionY;
	String farbe;
	public Rechteck(int breite, int hoehe, int positionX, int positionY, String farbe) {
		this.breite = breite;
		this.hoehe = hoehe;
		this.positionX = positionX;
		this.positionY = positionY;
		this.farbe = farbe;
	}

}
